package com.example.wafa.studentapp;

public class Marks {


    public  int  total , quize, mid, finals;

    public Marks() {
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getQuize() {
        return quize;
    }

    public int setQuize(int quize) {
        this.quize = quize;
        return quize;
    }

    public int getMid() {
        return mid;
    }

    public int setMid(int mid) {
        this.mid = mid;
        return mid;
    }

    public int getFinals() {
        return finals;
    }

    public int setFinals(int finals) {
        this.finals = finals;
        return finals;
    }
}
